type Category = keyof typeof CATEGORY_KEYWORDS;

export function generateCategory(title: string, content: string, subtitle?: string): string {
    const lowerTitle = title.toLowerCase();
    const lowerSubtitle = subtitle?.toLowerCase() || '';
    const lowerContent = content.toLowerCase();

    let bestCategory: Category | 'General' = 'General';
    let bestScore = 0;

    for (const [category, keywords] of Object.entries(CATEGORY_KEYWORDS) as [Category, readonly string[]][]) {
        let score = 0;

        for (const keyword of keywords) {
            if (lowerTitle.includes(keyword)) score += 3;
            if (lowerSubtitle.includes(keyword)) score += 2;
            if (lowerContent.includes(keyword)) score += 1;
        }

        if (score > bestScore) {
            bestScore = score;
            bestCategory = category;
        }
    }

    return bestScore > 2 ? bestCategory : 'General';
}
